		<h4><b>1. Menu Setting > Groups</b></h4>

			Menu setting groups merupakan halaman yang digunakan untuk membuat group dalam membatasi akses dari user sehingga hal apa saja yang diperbolehkan untuk menggunakan atau mengakses Content Management System (CMS). <br><br>

			<img src="<?php echo themeUrl();?>help/images/setting-list.jpeg" width="100%" /> <br><br>

			Untuk menambahkan group klik tombol <b>+add</b> di kanan atas sesuai dengan kebutuhan user. <br><br>

			<img src="<?php echo themeUrl();?>help/images/setting-add.jpeg" width="100%"/> <br><br>

			Pada form group administrator hanya diminta menginput nama group, penjelasan nama group dan status nama group.  <br><br>

		<b><i>Action Access Control List Administrator</i></b> <br><br>

			Ini adalah jendela dimana administrator dapat membatasi akses setiap user. Jika administrator ingin mengijinkan akses setiap user, administrator hanya menceklist setiap kotak pada action kemudian klik tombol <b>Save</b>. <br><br>
			
			<img src="<?php echo themeUrl();?>help/images/setting-access.jpeg" width="100%"/> <br><br>

			Keterangan : <br>
			1.	Search ( Mencari record data ) <br>
			2.	Add ( Menambahkan record data ) <br>
			3.	Edit ( Merubah / mengedit record data ) <br>
			4.	Delete ( Menghapus record data ) <br>
			5.	View ( View record data ) <br>
			6.	Akses ( Akses record data ) <br> <br>
