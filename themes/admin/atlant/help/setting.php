	<div class="col-md-6">
		<h4><b>2. Menu Setting > Users</b></h4>

			Menu setting users merupakan halaman yang digunakan untuk membuat username dan password yang digunakan pada halaman login Content Management System (CMS). <br><br>

			<img src="<?php echo themeUrl();?>help/images/user-list.jpeg" width="100%"/> <br><br>

			Pada halaman menu setting users, Tabel Manage User Login berisi semua data user seperti photo, username, email, nomor telp, domain, role, status dan action untuk menghapus atau mengedit data setiap user. Anda juga dapat menambahkan user baru dengan klik tombol <b>+add</b> dikanan atas. <br><br>

			<img src="<?php echo themeUrl();?>help/images/user-add.jpeg" width="100%"/> <br><br>

			Untuk menambahkan user baru pada form ini anda diminta untuk mengisi data lengkap setiap user seperti pada gambar diatas. <br><br>

		<h4><b>3. Menu Setting > Configuration</b></h4>

			Menu setting configuration merupakan halaman yang digunakan untuk melakukan konfigurasi dari template Content Managemen System (CMS). <br><br>

			<img src="<?php echo themeUrl();?>help/images/config.jpeg" width="100%"/> <br><br>

			Untuk sementara konfigurasi pada halaman menu setting configuration, administrator tidak dapat menambahkan apapun hanya merubah <i>menggunakan header</i> dari aplikasi tersebut. <br><br>

		<h4><b>4. Menu Setting > Log</b></h4>

			Menu setting log merupakan halaman yang digunakan untuk mengetahui history dari semua pengguna Content Management System (CMS) baik admin ataupun user dalam menjalankan aplikasi. <br><br>
			
			<img src="<?php echo themeUrl();?>help/images/log.jpeg" width="100%"/> <br><br>

			Pada halaman ini user tidak dapat menambahkan, menghapus atau merubah data log.
